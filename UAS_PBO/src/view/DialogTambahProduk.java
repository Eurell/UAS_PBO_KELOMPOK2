    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        try {
            if (txtNama.getText().isEmpty() || txtHarga.getText().isEmpty() || txtStock.getText().isEmpty()) {
                javax.swing.JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!");
                return;
            }

            model.Produk p = new model.Produk();
            p.setNama(txtNama.getText());
            p.setHarga(Integer.parseInt(txtHarga.getText()));
            p.setStock(Integer.parseInt(txtStock.getText()));
        
            // Ambil objek kategori yang dipilih user dari ComboBox
            model.Kategori k = (model.Kategori) cmbKategori.getSelectedItem();
            p.setKode_produk(k.getKode());

            if (kodeProdukEdit == 0) {
                // MODE TAMBAH
                if (produkDAO.insert(p)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Produk Berhasil Ditambahkan!");
                    this.dispose(); // Tutup pop-up
                }
            } else {
                // MODE EDIT
                p.setKode(kodeProdukEdit);
                if (produkDAO.update(p)) {
                    javax.swing.JOptionPane.showMessageDialog(this, "Produk Berhasil Diupdate!");
                    this.dispose(); // Tutup pop-up
                }
            }
        } catch (NumberFormatException e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Harga dan Stock wajib diisi dengan Angka!", "Error Input", javax.swing.JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

