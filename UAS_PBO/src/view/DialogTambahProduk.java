    // Method 1: Mengisi ComboBox otomatis dengan format "Kode - Kategori (Brand)"
    private void loadKategori() {
        cmbKategori.removeAllItems();
        for (model.Kategori k : kategoriDAO.getAllKategori()) {
            cmbKategori.addItem(k);
        }
    }

    // Method 2: Mengubah pop-up menjadi Mode Edit dan memilih ComboBox otomatis
    public void setUpdateMode(int kode, String nama, int harga, int stock, String namaKategori, String brand) {
        this.kodeProdukEdit = kode;
        txtNama.setText(nama);
        txtHarga.setText(String.valueOf(harga));
        txtStock.setText(String.valueOf(stock));

        // Mencari kategori yang pas di dalam ComboBox agar terpilih secara otomatis
        for (int i = 0; i < cmbKategori.getItemCount(); i++) {
            model.Kategori k = (model.Kategori) cmbKategori.getItemAt(i);
            if (k.getKategori().equals(namaKategori) && k.getBrand().equals(brand)) {
                cmbKategori.setSelectedIndex(i);
                break;
            }
        }
    }

