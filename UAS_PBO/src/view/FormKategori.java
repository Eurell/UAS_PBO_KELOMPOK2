    private void btninsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btninsertActionPerformed
       // Pop-up 1: Meminta Nama Kategori
        String inputKategori = javax.swing.JOptionPane.showInputDialog(this, "Masukkan Nama Kategori", "Tambah Kategori", javax.swing.JOptionPane.QUESTION_MESSAGE);
    
        // Validasi: Jika user klik 'Cancel' atau membiarkannya kosong, maka hentikan proses
        if (inputKategori == null || inputKategori.trim().isEmpty()) {
            return; 
        }

        // Pop-up 2: Meminta Nama Brand (Hanya muncul jika pop-up pertama berhasil diisi)
        String inputBrand = javax.swing.JOptionPane.showInputDialog(this, "Masukkan Brand", "Tambah Brand", javax.swing.JOptionPane.QUESTION_MESSAGE);

        // Validasi: Jika user klik 'Cancel' atau membiarkannya kosong, maka hentikan proses
        if (inputBrand == null || inputBrand.trim().isEmpty()) {
            return; 
        }

        // Jika keduanya berhasil diisi, masukkan ke database
        model.Kategori k = new model.Kategori();
        k.setKategori(inputKategori);
        k.setBrand(inputBrand);

        if (kategoriDAO.insert(k)) { 
            javax.swing.JOptionPane.showMessageDialog(this, "Kategori Berhasil Disimpan!");
            loadTable(); // Refresh tabel
        }
    }//GEN-LAST:event_btninsertActionPerformed

    private void btnupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnupdateActionPerformed
        int baris = tblKategori.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data di tabel terlebih dahulu yang ingin diedit!");
            return;
        }

        // Ambil ID dan data lama dari tabel
        int kode = (int) tblKategori.getValueAt(baris, 0);
        String kategoriLama = tblKategori.getValueAt(baris, 1).toString();
        String brandLama = tblKategori.getValueAt(baris, 2).toString();

        // Pop-up 1: Edit Kategori
        String inputKategori = (String) javax.swing.JOptionPane.showInputDialog(this, "Update Nama Kategori:", "Update Kategori", javax.swing.JOptionPane.QUESTION_MESSAGE, null, null, kategoriLama);
    
        // Validasi batal/kosong
        if (inputKategori == null || inputKategori.trim().isEmpty()) {
            return; 
        }

        // Pop-up 2: Edit Brand (Ada variabel 'brandLama' sebagai nilai awal)
        String inputBrand = (String) javax.swing.JOptionPane.showInputDialog(this, "Update Brand:", "Update Brand", javax.swing.JOptionPane.QUESTION_MESSAGE, null, null, brandLama);

        // Validasi batal/kosong
        if (inputBrand == null || inputBrand.trim().isEmpty()) {
            return; 
        }

        // Proses update ke database
        model.Kategori k = new model.Kategori();
        k.setKode(kode);
        k.setKategori(inputKategori);
        k.setBrand(inputBrand);

        if (kategoriDAO.update(k)) {
            javax.swing.JOptionPane.showMessageDialog(this, "Kategori Berhasil Diupdate!");
            loadTable();
        }
    }//GEN-LAST:event_btnupdateActionPerformed

