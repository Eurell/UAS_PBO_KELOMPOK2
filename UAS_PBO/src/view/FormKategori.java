    private void loadTable() {
        javax.swing.table.DefaultTableModel model = (javax.swing.table.DefaultTableModel) tblKategori.getModel();
        model.setRowCount(0); // Kosongkan tabel
        
        // Ambil data dari DAO lalu susun ke tabel
        for (model.Kategori k : kategoriDAO.getAllKategori()) {
            Object[] baris = {
                k.getKode(), 
                k.getKategori(), 
                k.getBrand()
            };
            model.addRow(baris);
            adjustTableHeight();
        }
    }
    public void adjustTableHeight() {
        int rowCount = tblKategori.getRowCount();
        int rowHeight = tblKategori.getRowHeight();
        int headerHeight = tblKategori.getTableHeader().getPreferredSize().height;    
        int totalHeight = (rowCount * rowHeight) + headerHeight + 5; 
    
        jScrollPane1.setPreferredSize(new java.awt.Dimension(jScrollPane1.getWidth(), totalHeight));
        jScrollPane1.revalidate();
        jScrollPane1.repaint();
    }

