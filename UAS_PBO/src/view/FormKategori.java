    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int baris = tblKategori.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih data di tabel yang ingin dihapus!");
            return;
        }
    
        int kode = (int) tblKategori.getValueAt(baris, 0);

        int konfirmasi = javax.swing.JOptionPane.showConfirmDialog(this, "Yakin ingin menghapus kategori ini?", "Konfirmasi", javax.swing.JOptionPane.YES_NO_OPTION);
        if (konfirmasi == javax.swing.JOptionPane.YES_OPTION) {
            // Jika kategori masih dipakai di produk, KategoriDAO akan memblokirnya secara otomatis
            if (kategoriDAO.delete(kode)) {
                javax.swing.JOptionPane.showMessageDialog(this, "Kategori Berhasil Dihapus!");
                loadTable();
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

