    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int baris = tblProduk.getSelectedRow();
        if (baris == -1) {
            JOptionPane.showMessageDialog(this, "Pilih baris produk yang ingin dihapus terlebih dahulu!");
            return;
        }
    
        // Ambil nilai "Kode" dari kolom ke-0 pada baris yang dipilih
        int kode = (int) tblProduk.getValueAt(baris, 0);
    
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Apakah Anda yakin ingin menghapus produk ini?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        if (konfirmasi == JOptionPane.YES_OPTION) {
            if (produkDAO.delete(kode)) {
                JOptionPane.showMessageDialog(this, "Produk berhasil dihapus!");
                loadTable(); // Refresh tabel
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int baris = tblProduk.getSelectedRow();
        if (baris == -1) {
            javax.swing.JOptionPane.showMessageDialog(this, "Pilih produk yang ingin diedit di tabel terlebih dahulu!");
            return;
        }

        // Ambil data dari tabel sesuai urutan kolom yang kita buat:
        // 0:Kode, 1:Nama, 2:Harga, 3:Stock, 4:Kategori, 5:Brand
        int kode = (int) tblProduk.getValueAt(baris, 0);
        String nama = tblProduk.getValueAt(baris, 1).toString();
        int harga = (int) tblProduk.getValueAt(baris, 2);
        int stock = (int) tblProduk.getValueAt(baris, 3);
        String kategori = tblProduk.getValueAt(baris, 4).toString();
        String brand = tblProduk.getValueAt(baris, 5).toString();

        // Buka pop-up DialogTambahProduk dan kirim data ke method setUpdateMode
        DialogTambahProduk dialog = new DialogTambahProduk(this, true);
        dialog.setTitle("Update Produk");
        dialog.setUpdateMode(kode, nama, harga, stock, kategori, brand);
        dialog.setVisible(true); 
        
        // Refresh tabel otomatis setelah pop-up ditutup
        loadTable();
    }//GEN-LAST:event_btnUpdateActionPerformed

