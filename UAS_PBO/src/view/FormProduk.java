    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblProduk.getModel();
        model.setRowCount(0); // Kosongkan tabel dulu
        
        // produkDAO.getAllProduk() mengembalikan array dengan urutan dari SQL:
        // [0]=kode, [1]=nama, [2]=kategori, [3]=brand, [4]=harga, [5]=stock, [6]=kode_produk
        
        for (Object[] row : produkDAO.getAllProduk()) {
            // Susun ulang urutan agar pas dengan kolom tabel di form:
            // Kode, Nama Produk, Harga, Stock, Kategori, Brand
            Object[] tableRow = {
                row[0], // Kode
                row[1], // Nama Produk
                row[4], // Harga
                row[5], // Stock
                row[2], // Kategori
                row[3]  // Brand
            };
            model.addRow(tableRow);
            adjustTableHeight();
        }
    }
    
    public void adjustTableHeight() {
        int rowCount = tblProduk.getRowCount();
        int rowHeight = tblProduk.getRowHeight();
        int headerHeight = tblProduk.getTableHeader().getPreferredSize().height;    
        int totalHeight = (rowCount * rowHeight) + headerHeight + 5; 
    
        jScrollPane1.setPreferredSize(new java.awt.Dimension(jScrollPane1.getWidth(), totalHeight));
        jScrollPane1.revalidate();
        jScrollPane1.repaint();
    }

